          <circle cx="10.5" cy="10.5" r="6.5" />
          <line x1="20" y1="20" x2="15.3" y2="15.3" />
        </svg>
      </span>
      <input
        bind:this={inputEl}
        bind:value={query}
        type="text"
        placeholder="Search articles and recipes…"
        aria-label="Search"
        role="combobox"
        aria-expanded={results.length > 0}
        aria-controls="search-result-list"
        aria-autocomplete="list"
        aria-activedescendant={results[selectedIndex] ? `search-result-${selectedIndex}` : undefined}
        autocomplete="off"
        spellcheck="false"
      />
      <button class="esc-btn" on:click={closeModal} aria-label="Close search">
        <span class="esc-label" aria-hidden="true">esc</span>
        <span class="close-label" aria-hidden="true">✕</span>
      </button>
    </div>

    <div aria-live="polite" class="sr-only">
      {#if results.length > 0}{results.length} result{results.length === 1 ? '' : 's'}{/if}
    </div>

    {#if devMode}
      <div class="state-msg">Search is only available in production builds.</div>
    {:else if !query.trim()}
      <div class="empty-state">
        <span class="empty-badge" aria-hidden="true">
          <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.75" stroke-linecap="round" stroke-linejoin="round">
            <circle cx="10.5" cy="10.5" r="6.5" />
            <line x1="20" y1="20" x2="15.3" y2="15.3" />
          </svg>
        </span>
        <p class="empty-title">Equal parts code and cooking.</p>
        <div class="empty-chips">
          {#each suggestions as s}
            <button type="button" class="chip" on:click={() => trySuggestion(s)}>{s}</button>
          {/each}
        </div>
      </div>
    {:else if results.length === 0}
      <div class="state-msg">No results for '{query}' — maybe it's not written yet.</div>
    {:else}
      <div class="modal-body">
        <ul
          id="search-result-list"
          class="result-list"
          role="listbox"
          aria-label="Search results"
        >
          {#each results as result, i}
            <li
              id="search-result-{i}"
              class="result-item"
              class:active={i === selectedIndex}
              role="option"
              aria-selected={i === selectedIndex}
              on:mouseenter={() => (selectedIndex = i)}
              on:click={() => (window.location.href = result.url)}
              on:keydown={(e) => { if (e.key === 'Enter') window.location.href = result.url; }}
            >
              <span class="dot {dotClass(result.url)}" aria-hidden="true"></span>
              <span class="result-title">{result.meta.title}</span>
            </li>
          {/each}
        </ul>
        <div class="preview-panel" aria-hidden="true">
          {#if results[selectedIndex]}
            {@const r = results[selectedIndex]}
            <div class="preview-type">{typeLabel(r.url)}</div>
            <div class="preview-title">{r.meta.title}</div>
            <!-- Safe: Pagefind excerpt contains only <mark> highlight tags, no user-supplied HTML -->
            <!-- eslint-disable-next-line svelte/no-at-html-tags -->
            <div class="preview-excerpt">{@html r.excerpt}</div>
            {#if r.meta.tags}
              <div class="preview-tags">{r.meta.tags}</div>
            {/if}
          {/if}
        </div>
      </div>
    {/if}

    <div class="modal-footer" aria-hidden="true">
      <span><kbd>↑↓</kbd> navigate</span>
      <span><kbd>↵</kbd> open</span>
      <span><kbd>esc</kbd> close</span>
    </div>
  </div>
{/if}

