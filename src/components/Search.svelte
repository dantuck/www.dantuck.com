<script lang="ts">
  import { onMount, onDestroy, tick } from 'svelte';

  type SearchResult = {
    url: string;
    meta: { title: string; tags?: string };
    excerpt: string;
  };

  let open = false;
  let query = '';
  let results: SearchResult[] = [];
  let selectedIndex = 0;
  let devMode = false;
  let inputEl: HTMLInputElement | undefined;
  let modalEl: HTMLElement | undefined;
  let triggerEl: HTMLElement | null = null;
  let searchId = 0;

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let pagefind: any;
  let pagefindLoaded = false;

  const suggestions = ['tailscale', 'nix', 'homelab', 'whiskey', 'salsa', 'dinner'];

  function trySuggestion(s: string) {
    query = s;
    inputEl?.focus();
  }

  async function loadPagefind() {
    if (pagefindLoaded || devMode) return;
    try {
      // Path split into variable so Vite does not statically resolve it at build time.
      // The file is generated by pagefind post-build (or served from dist/ in dev via the Vite plugin).
      const url = '/pagefind' + '/pagefind.js';
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      pagefind = await import(/* @vite-ignore */ url as any);
      pagefindLoaded = true;
    } catch {
      devMode = true;
    }
  }

  async function openModal(trigger: HTMLElement | null = null) {
    if (typeof document === 'undefined') return;
    triggerEl = trigger ?? (document.activeElement as HTMLElement | null);
    open = true;
    await tick();
    inputEl?.focus();
    await loadPagefind();
  }

  function closeModal() {
    open = false;
    query = '';
    results = [];
    selectedIndex = 0;
    triggerEl?.focus();
    triggerEl = null;
    if (typeof document === 'undefined') return;
    document.dispatchEvent(new CustomEvent('close-search'));
  }

  async function runSearch(q: string) {
    const id = ++searchId;
    if (!pagefindLoaded || devMode || !q.trim()) {
      results = [];
      return;
    }
    const res = await pagefind.search(q);
    if (id !== searchId) return;
    const raw = await Promise.all(res.results.slice(0, 12).map((r: any) => r.data()));
    if (id !== searchId) return;
    results = raw;
    selectedIndex = 0;
  }

  $: runSearch(query);

  function handleGlobalKeydown(e: KeyboardEvent) {
    if ((e.metaKey || e.ctrlKey) && e.key === 'k') {
      e.preventDefault();
      if (open) closeModal();
      else openModal();
    }
  }

  function handleModalKeydown(e: KeyboardEvent) {
    if (e.key === 'Escape') {
      e.stopPropagation();
      closeModal();
    } else if (e.key === 'ArrowDown') {
      e.preventDefault();
      selectedIndex = Math.min(selectedIndex + 1, results.length - 1);
    } else if (e.key === 'ArrowUp') {
      e.preventDefault();
      selectedIndex = Math.max(selectedIndex - 1, 0);
    } else if (e.key === 'Enter' && results[selectedIndex]) {
      window.location.href = results[selectedIndex].url;
    } else if (e.key === 'Tab') {
      const focusable = modalEl?.querySelectorAll<HTMLElement>('input, button');
      if (!focusable?.length) return;
      const first = focusable[0];
      const last = focusable[focusable.length - 1];
      if (e.shiftKey && document.activeElement === first) {
        e.preventDefault();
        last.focus();
      } else if (!e.shiftKey && document.activeElement === last) {
        e.preventDefault();
        first.focus();
      }
    }
  }

  function handleOpenSearch() {
    openModal();
  }

  function typeLabel(url: string): string {
    return url.includes('/recipe/') ? 'Recipe' : 'Article';
  }

  function dotClass(url: string): string {
    return url.includes('/recipe/') ? 'dot-recipe' : 'dot-article';
  }

  onMount(() => {
    document.addEventListener('keydown', handleGlobalKeydown);
    document.addEventListener('open-search', handleOpenSearch);
  });

  onDestroy(() => {
    if (typeof document === 'undefined') return;
    document.removeEventListener('keydown', handleGlobalKeydown);
    document.removeEventListener('open-search', handleOpenSearch);
  });
</script>

{#if open}
  <div class="backdrop" on:click={closeModal} aria-hidden="true"></div>
  <div
